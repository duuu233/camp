function createAgreementDocument({ id, title, kicker, sourceFile, text }) {
  return {
    id,
    title,
    kicker,
    sourceFile,
    lines: text
      .trim()
      .split(/\r?\n/)
      .map(line => line.trim())
      .filter(Boolean)
  }
}

